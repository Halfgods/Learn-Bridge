MIN_WORD_LEN = 3
MAX_SUGGESTIONS = 3
MAX_DISTANCE = 2


def suggest(word: str) -> list[str]:
    """Return suggested corrections for a misspelled word."""
    word = word.lower()
    if word in DICTIONARY:
        return []
    candidates = []
    for entry in DICTIONARY:
        dist = levenshtein(word, entry)
        if 0 < dist <= MAX_DISTANCE:
            candidates.append((dist, entry))
    candidates.sort(key=lambda x: (x[0], x[1]))
    return [w for _, w in candidates[:MAX_SUGGESTIONS]]


def check_text(text: str) -> list[dict]:
    """Check a full text for spelling errors.
    Returns list of {word, suggestions} for each misspelled word.
    """
    words = re.findall(r"[a-zA-Z]+", text)
    errors = []
    for w in words:
        if len(w) < MIN_WORD_LEN:
            continue
        if w.lower() in DICTIONARY:
            continue
        sugs = suggest(w.lower())
        if not sugs:
            # Skip words with no close match (likely proper nouns)
            continue
        errors.append({"word": w, "suggestions": sugs})
    return errors
